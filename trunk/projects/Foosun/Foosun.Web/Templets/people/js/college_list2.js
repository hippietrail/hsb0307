     function multiLevelMenu_college1(level){
        obj=document.getElementById(arrSel_college1[level]);
		if(obj)
		{
			subMenu_Name = "menu_college";
			for(i=0;i<level;i++)
			{
				subMenu_Name += "[" + (document.getElementById(arrSel_college1[i]).selectedIndex*2 + 1) + "]";
			}
			
			subMenu = eval(subMenu_Name);
			//alert (subMenu_Name);
			//if(!subMenu) return false;
			
			
			sel_len = obj.length;
			for(i=0;i<sel_len;i++)
			{
				obj.options.remove(0);
			}

			new_sel_len = Math.floor(subMenu.length/2);

			for(i=0;i<new_sel_len;i++)
			{
				obj.options.add(new Option(subMenu[i*2],subMenu[i*2+1]));
			}
			
			obj.onchange = Function("multiLevelMenu_college1(" + (level+1) + ")");

			multiLevelMenu_college1(level+1);
		}
		else
		{

			return false;
		}	
     }
     
